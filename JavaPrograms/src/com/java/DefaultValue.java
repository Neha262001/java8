package com.java;

public class DefaultValue {
	static int i;
	static byte b;
	static char c;
	static float f;
	static String s;
	static long l;
	static double d;
	static boolean bol;
	public static void main(String[] args) {
		int a=3;
	    char ch='p';
	    float fl = 23.8f;
	    String st="NEHA";//literal
	    String s1 = new String("Welcome");//object
	    long ln=87655;
	    double nn = 23.98765;
	    boolean bb = true;
	    System.out.println(s1);
	    System.out.println("default value of string is:" + s1);

	    System.out.println("default value of integer is:" + i);
	    System.out.println("default value of byte is:" + b);
	    System.out.println("default value of char is:" + c);
	    System.out.println("default value of float is:" + f);
	    System.out.println("default value of String is:" + s);
	    System.out.println("default value of long is:" + l);
	    System.out.println("default value of double is:" + d);
	    System.out.println("default value of boolean is:" + bol);

		System.out.println(a);
		System.out.println(ch);
		System.out.println(fl);
		System.out.println(st);
		System.out.println(ln);
		System.out.println(nn);
	    System.out.println(bb);




	}

	

}
